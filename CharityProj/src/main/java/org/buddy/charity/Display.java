package org.buddy.charity;

public class Display {
	public String name; 
	public String score; 
	public String location; 
	public String description; 
	public String category; 
	public String url; 
	
	Display(String n, String s, String l, String d, String c, String u) {
		name = n; 
		score = s; 
		location = l; 
		description = d; 
		category = c; 
		url = u; 
	}
}
